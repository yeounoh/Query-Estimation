
public class Person{
	private int id;
	private int age;
	private int coffee;
	
	public Person(int id, int age, int coffee){
		this.id = id;
		this.age = age;
		this.coffee = coffee;
	}
	
	public int getID(){
		return id;
	}
	
	public int getAge(){
		return age;
	}
	
	public int getCoffee(){
		return coffee;
	}
}
